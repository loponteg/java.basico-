public class variaveis {
    public static void main(String[] args) {
        // variaveis
        int a = 20;
        System.out.println("A idade é: " + a);
        boolean b = true;
        System.out.println("É estudante? " + b);   
        double c = 1.93;
        System.out.println("A altura é: " + c);
        char d = 'M';
        System.out.println("O genero é: " + d);
        String e = "Gabriel";
        System.out.println("O nome é: " + e);


    }
}
